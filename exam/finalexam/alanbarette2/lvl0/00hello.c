// Write a program that displays "Hello World!" followed by a \n.

// Example:

// $>./hello
// Hello World!
// $>./hello | cat -e
// Hello World!$
// $>

// ======================================****1******=========================================

#include <unistd.h>

int main(void)
{
    char c[] = "Hello World!";
    write(1, &c, sizeof c);
    write(1, "\n", 1);
    return(0);
}

